void uint32_to_string(uint32_t number, char str[])
{
  int i = 0;
  char temp[12];

  if (number == 0)
  {
    str[i++] = '0';
    str[i++] = '\n';
    str[i] = '\0';
    return;
  }

  while (number > 0)
  {
    temp[i++] = (number % 10) + '0';
    number /= 10;
  }

  int j;
  for (j = 0; j < i; j++)
  {
    str[j] = temp[i - j - 1];
  }
  str[j++] = '\n';
  str[j] = '\0';
}
